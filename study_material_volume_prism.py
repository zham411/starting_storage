length = int(input("Enter an integer that represents length in feet."))
width = int(input("Enter an integer that represents width in feet."))
height = int(input("Enter an integer that represents width in feet."))


def calculate_volume (l, w, h):
    return l * w * h




print ("The volume of the rectangular prism is " + str(calculate_volume(length, width, height)) + " cubic feet.")














